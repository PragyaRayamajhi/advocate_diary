    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        // Get the selected row
    int selectedRow = tblStudent.getSelectedRow();

    // Check if a row is selected
    if (selectedRow != -1) {
        // Get updated values from text fields
        String caseId = txtCaseId.getText();
        String caseName = txtCaseName.getText();
        String caseType = cmbType.getSelectedItem().toString();
        String assignedLawyer = txtAssignedLawyer.getText();
        String caseStatus = cmbStatus.getSelectedItem().toString();
        String courtName = txtCourtName.getText();
        String filingDate = txtFilingDate.getText();
        String clientName = txtClientName.getText();
        String contactNo = txtContactNo.getText();

        // Validate the inputs using the ValidationUtil methods
        if (ValidationUtil.isValidCaseId(caseId, lblError) &&
            ValidationUtil.isValidCaseName(caseName, lblError) &&
            ValidationUtil.isValidAssignedLawyer(assignedLawyer, lblError) &&
            ValidationUtil.isValidCourtName(courtName, lblError) &&
            ValidationUtil.isValidFilingDate(filingDate, lblError) &&
            ValidationUtil.isValidClientName(clientName, lblError) &&
            ValidationUtil.isValidContactNo(contactNo, lblError)) {

            // Update the selected row in the table
            tblStudent.setValueAt(caseId, selectedRow, 0);
            tblStudent.setValueAt(caseName, selectedRow, 1);
            tblStudent.setValueAt(caseType, selectedRow, 2);
            tblStudent.setValueAt(assignedLawyer, selectedRow, 3);
            tblStudent.setValueAt(caseStatus, selectedRow, 4);
            tblStudent.setValueAt(courtName, selectedRow, 5);
            tblStudent.setValueAt(filingDate, selectedRow, 6);
            tblStudent.setValueAt(clientName, selectedRow, 7);
            tblStudent.setValueAt(contactNo, selectedRow, 8);

            // Update the caseList
            AdvocateDiaryModel updatedCase = new AdvocateDiaryModel(
                Integer.parseInt(caseId), caseName, caseType, assignedLawyer,
                caseStatus, courtName, filingDate, clientName, contactNo
            );
            studentList.set(selectedRow, updatedCase);

            // Show success message
            JOptionPane.showMessageDialog(this, "Case updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Show error message
            JOptionPane.showMessageDialog(this, "Please enter valid data.", "Validation Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        // No row selected error
        JOptionPane.showMessageDialog(this, "Please select a case to update.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

