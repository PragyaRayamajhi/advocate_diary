    private boolean isCaseIdExists(int caseId) {
    // Replace 'droneList' with the actual list or database storing drone data
    for (AdvocateDiaryModel drone : studentList) { 
        if (drone.getCaseId() == caseId) {
            return true; // Drone ID already exists
        }
    }
    return false; // Drone ID is unique
}
    
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        // Get the input data from the UI fields
        String caseId = txtCaseId.getText();
        String caseName = txtCaseName.getText();
        String caseType = cmbType.getSelectedItem().toString();
        String assignedLawyer = txtAssignedLawyer.getText();
        String caseStatus = cmbStatus.getSelectedItem().toString();
        String courtName = txtCourtName.getText();
        String filingDate = txtFilingDate.getText();
        String clientName = txtClientName.getText();
        String contactNo = txtContactNo.getText();

        // Validate the inputs using the ValidationUtil methods
        if (ValidationUtil.isValidCaseId(caseId, lblError) &&
            ValidationUtil.isValidCaseName(caseName, lblError) &&
            ValidationUtil.isValidAssignedLawyer(assignedLawyer, lblError) &&
            ValidationUtil.isValidCourtName(courtName, lblError) &&
            ValidationUtil.isValidFilingDate(filingDate, lblError) &&
            ValidationUtil.isValidClientName(clientName, lblError) &&
            ValidationUtil.isValidContactNo(contactNo, lblError)) {

            // Create a new CaseModel instance
            int caseIdValue = Integer.parseInt(caseId);
             if (isCaseIdExists(caseIdValue)) { // Call the method to check duplicate IDs
                JOptionPane.showMessageDialog(this, 
                        "Case ID " + caseId + " already exists and cannot be added!", 
                        "Duplicate Entry", 
                        JOptionPane.ERROR_MESSAGE);
            } else{
            
            AdvocateDiaryModel caseModel = new AdvocateDiaryModel(caseIdValue, caseName, caseType, assignedLawyer, caseStatus, courtName, filingDate, clientName, contactNo);

            // Register the case
            registerCase(caseModel);

            // Show a success message
            JOptionPane.showMessageDialog(this, "Case added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
             }

        } else {
            JOptionPane.showMessageDialog(this, "Please enter valid data.", "Validation Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblStudent.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
            model.removeRow(selectedRow);
            studentList.remove(selectedRow);
            JOptionPane.showMessageDialog(this, "Case removed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Please select a case to remove.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

